    def _format_td(self, seconds: int | float) -> str:
        total = max(0, int(seconds))
        if total <= 0:
            return self.strings["now"]
        hours, rem = divmod(total, 3600)
        mins, secs = divmod(rem, 60)
        parts = []
        lang = self._lang_code()
        if lang in {"en", "de", "neofit", "tiktok", "leet", "uwu"}:
            if hours:
                parts.append(f"{hours}h")
            if mins:
                parts.append(f"{mins}m")
            if secs and not hours:
                parts.append(f"{secs}s")
        elif lang == "jp":
            if hours:
                parts.append(f"{hours}śôāťĖď")
            if mins:
                parts.append(f"{mins}ŚąÜ")
            if secs and not hours:
                parts.append(f"{secs}Áßí")
        else:
            if hours:
                parts.append(f"{hours}—á")
            if mins:
                parts.append(f"{mins}–ľ–ł–Ĺ")
            if secs and not hours:
                parts.append(f"{secs}—Ā–Ķ–ļ")
        return " ".join(parts) if parts else self.strings["now"]

