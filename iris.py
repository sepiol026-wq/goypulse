def _parse_icoins(text: str) -> int:
    m = re.search(r"([0-9][0-9 ]*)\s*i[¬Ęc]", text, re.IGNORECASE)
    return int(m.group(1).replace(" ", "")) if m else 0


def _parse_cooldown_seconds(text: str) -> int | None:
    chunk = re.search(r"—á–Ķ—Ä–Ķ–∑\s+([^\n\r.!]+)", text.lower())
    if not chunk:
        return None
    part = chunk.group(1)
    hours = re.search(r"(\d+)\s*—á–į—Ā", part)
    mins = re.search(r"(\d+)\s*–ľ–ł–Ĺ", part)
    secs = re.search(r"(\d+)\s*—Ā–Ķ–ļ", part)
    total = (
        int(hours.group(1)) * 3600 if hours else 0
    ) + (
        int(mins.group(1)) * 60 if mins else 0
    ) + (
        int(secs.group(1)) if secs else 0
    )
    return total or None


def _parse_all_loads(text: str) -> dict[str, float]:
    result = {}
    for bot in IRIS_BOTS:
        match = re.search(
            rf"t\.me/{re.escape(bot)}.{{0,450}}?–ó–į–≥—Ä—É–∂–Ķ–Ĺ:\s*([\d,.]+)\s*%",
            text,
            re.IGNORECASE | re.DOTALL,
        )
        if match:
            result[bot] = float(match.group(1).replace(",", "."))
    return result


