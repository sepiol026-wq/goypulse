    async def _download_soundcloud_track(self, track_url: str, out_path: str) -> dict:
        async with requests.AsyncSession(impersonate="chrome124") as ses:
            h_resp = await ses.get(track_url)
            if h_resp.status_code != 200:
                raise ValueError("SoundCloud page not reachable")

            html = h_resp.text
            c_id = await self._get_client_id(ses, html)

            h_m = re.search(r'window\.__sc_hydration\s*=\s*(\[.*?\]);</script>', html)
            if not h_m:
                raise ValueError("No hydration payload")

            payload = json.loads(h_m.group(1))
            t_d = next((i.get("data") for i in payload if i.get("hydratable") == "sound"), None)
            if not t_d or t_d.get("kind") != "track":
                raise ValueError("Not a track")

            art = t_d.get("artwork_url") or t_d.get("user", {}).get("avatar_url")
            if art:
                art = art.replace("-large.jpg", "-t500x500.jpg")
            tr = t_d.get("media", {}).get("transcodings", [])
            if not tr:
                raise ValueError("No transcodings")

            s_info = next((t for t in tr if t.get("format", {}).get("protocol") == "progressive"), tr[0])
            s_url = s_info.get("url") + f"?client_id={c_id}" + (f"&track_authorization={t_d.get('track_authorization')}" if t_d.get("track_authorization") else "")

            s_resp = await ses.get(s_url)
            if s_resp.status_code != 200:
                raise ValueError("Stream metadata failed")

            s_json = s_resp.json()
            media_url = s_json.get("url")
            if not media_url:
                raise ValueError("No stream url")

            protocol = s_info.get("format", {}).get("protocol")
            if protocol == "progressive":
                m_resp = await ses.get(media_url)
                if m_resp.status_code != 200:
                    raise ValueError("Audio download failed")
                with open(out_path, "wb") as file_obj:
                    file_obj.write(m_resp.content)
            else:
                m3_resp = await ses.get(media_url)
                if m3_resp.status_code != 200:
                    raise ValueError("Playlist download failed")
                chunks = [l.strip() for l in m3_resp.text.splitlines() if l and not l.startswith("#")]
                if not chunks:
                    raise ValueError("Empty HLS playlist")

                base_url = media_url.rsplit("/", 1)[0] + "/"
                sem = asyncio.Semaphore(10)

                async def _fetch_segment(url: str, index: int):
                    if not url.startswith("http"):
                        url = base_url + url
                    async with sem:
                        resp = await ses.get(url)
                        if resp.status_code != 200:
                            raise ValueError(f"Segment {index} failed")
                        return index, resp.content

                segments = await asyncio.gather(*[_fetch_segment(u, i) for i, u in enumerate(chunks)])
                segments.sort(key=lambda x: x[0])
                with open(out_path, "wb") as file_obj:
                    for _, data in segments:
                        file_obj.write(data)

            return {
                "id": str(t_d.get("id") or ""),
                "title": t_d.get("title") or "Unknown",
                "uploader": (
                    t_d.get("user", {}).get("username")
                    or t_d.get("publisher_metadata", {}).get("artist")
                    or t_d.get("user", {}).get("full_name")
                    or "Unknown"
                ),
                "duration": int(t_d.get("duration") or 0) // 1000,
                "thumbnail": art,
                "webpage_url": track_url,
                "source": "sc",
            }

